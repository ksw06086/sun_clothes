package mvc.member.vo;


public class PluspayVO {
	// 멤버변수
	private String gname;     
    private String gid;
    private int cnt;
    private int useplus;
    private int myplus;
    
    // 생성자
    public PluspayVO() {}

    // setter/getter
	public String getGname() {
		return gname;
	}

	public void setGname(String gname) {
		this.gname = gname;
	}

	public String getGid() {
		return gid;
	}

	public void setGid(String gid) {
		this.gid = gid;
	}

	public int getCnt() {
		return cnt;
	}

	public void setCnt(int cnt) {
		this.cnt = cnt;
	}

	public int getUseplus() {
		return useplus;
	}

	public void setUseplus(int useplus) {
		this.useplus = useplus;
	}

	public int getMyplus() {
		return myplus;
	}

	public void setMyplus(int myplus) {
		this.myplus = myplus;
	}

	
    
    
    
}
